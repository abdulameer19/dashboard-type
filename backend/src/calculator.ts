const mathOperations = {
  sum: function (a: number, b: number): number {
    return a + b;
  },

  diff: function (a: number, b: number): number {
    return a - b;
  },

  product: function (a: number, b: number): number {
    return a * b;
  },

  divide: function (a: number, b: number): number {
    return a / b;
  },
};

export = mathOperations;
